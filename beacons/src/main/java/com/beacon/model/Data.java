package com.beacon.model;

public class Data {
	
	private String temperature;
	private String ambient_light_level;
	private String date;
	
	public String getTemperature() {
		return temperature;
	}
	public void setTemperature(String temperature) {
		this.temperature = temperature;
	}
	public String getAmbient_Light_Level() {
		return ambient_light_level;
	}
	public void setAmbient_Light_Level(String ambient_light_level) {
		this.ambient_light_level = ambient_light_level;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}

}
